#include <cstdlib>
#include <iostream>
  
using namespace std;
  
int main()
{
    int val1, val2;
  
    /// finding absolute value using
    /// abs() function.
    val1 = abs(22);
    val2 = abs(-43);
  
    cout << "abs(22) = " << val1 << "\n";
    cout << "abs(-43) = " << val2 << "\n";
    return 0;
}
